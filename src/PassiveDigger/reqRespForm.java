    public reqRespForm() {
        initComponents();
        try {
            requestViewer = callbacks.createMessageEditor(this, false);
            responseViewer = callbacks.createMessageEditor(this, false);
            tabs.addTab("Request", requestViewer.getComponent());
            tabs.addTab("Response", responseViewer.getComponent());
            requestViewer.setMessage(thisReqResp.getRequest(), true);
            responseViewer.setMessage(thisReqResp.getResponse(), false);
        } catch (Exception e) {
            BurpExtender.output.println("Exception");
            BurpExtender.output.println(e.getMessage());
            BurpExtender.output.println(e.toString());
        }
        setTitle();
    }

